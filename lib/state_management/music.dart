class AudioModel {
  final String title;
  final String artist;
  final String album;
  final String genre;
  final String release;
  final String audio;
  final String coverPhoto;
  final String id;

  AudioModel({
    required this.title,
    required this.artist,
    required this.album,
    required this.genre,
    required this.release,
    required this.audio,
    required this.coverPhoto,
    required this.id,
  });
}
